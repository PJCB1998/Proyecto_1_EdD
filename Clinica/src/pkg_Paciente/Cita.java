package pkg_Paciente;

public class Cita {

	private Sala sala;
	private Paciente paciente;
	private String fecha, hora;
	private double precio;
	public Sala getSala() {
		return sala;
	}
	public Paciente getPaciente() {
		return paciente;
	}
	public String getFecha() {
		return fecha;
	}
	public String getHora() {
		return hora;
	}
	public double getPrecio() {
		return precio;
	}
	
	
}
